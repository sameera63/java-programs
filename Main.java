public class Main {
	    public static int solve(int a, int b) {
	        return a * b + 3;
	    }
       public static void main(String[] args) {
	        System.out.println("6,5 = " + solve(6, 5));   
	        System.out.println("7,2 = " + solve(7, 2));   
	        System.out.println("11,4 = " + solve(11, 4)); 
	        System.out.println("3,8 = " + solve(3, 8));   
	    }
	}
